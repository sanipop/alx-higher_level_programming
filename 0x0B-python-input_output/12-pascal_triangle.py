#!/usr/bin/python3
"""A script that makes a pascal triangle."""


def pascal_triangle(n):
    """Function that cal pascal triangle.

    Returns an output of pascal triangle.
    """
    if n <= 0:
        return []

    triangles = [[1]]
    while len(triangles) != n:
        tri = triangles[-1]
        tmp = [1]
        for i in range(len(tri) - 1):
            tmp.append(tri[i] + tri[i + 1])
        tmp.append(1)
        triangles.append(tmp)
    return triangles
